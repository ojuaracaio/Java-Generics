
public class Fruta {
	String tipo;
	double preco;
	
	public Fruta(String tipo, double preco) {
		this.tipo = tipo;
		this.preco = preco;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public double getPreco() {
		return preco;
	}
	public void setPreco(double preco) {
		this.preco = preco;
	}
	
}
